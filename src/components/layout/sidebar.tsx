// Sidebar stats card using real data from API
function SidebarStatsCard() {
  const { stats, isLoading } = useGlobalStats();

  return (
    <div className="p-4">
      <div className="p-4 rounded-xl bg-gradient-to-br from-primary-500/10 to-purple-500/10 border border-primary-500/20">
        <div className="text-xs text-white/40 mb-1">Total Value Locked</div>
        {isLoading ? (
          <div className="h-7 w-24 bg-white/10 animate-pulse rounded" />
        ) : (
          <div className="text-xl font-bold text-white">{stats.formattedTvl}</div>
        )}
        <div className={cn(
          "text-xs mt-1",
          stats.totalPnl >= 0 ? "text-emerald-400" : "text-red-400"
        )}>
          {stats.tvlChange} (24h)
        </div>
      </div>
    </div>
  );
}

interface SidebarProps {
  isOpen: boolean;
  onClose: () => void;
}

export function Sidebar({ isOpen, onClose }: SidebarProps) {
  const pathname = usePathname();
  const [isMobile, setIsMobile] = React.useState(false);

  // Detect mobile vs desktop
  React.useEffect(() => {
    const checkMobile = () => {
      setIsMobile(window.innerWidth < 1024);
    };
    checkMobile();
    window.addEventListener('resize', checkMobile);
    return () => window.removeEventListener('resize', checkMobile);
  }, []);

  return (
    <>
      {/* Backdrop - mobile only */}
      {isOpen && isMobile && (
        <motion.div
          initial={{ opacity: 0 }}
          animate={{ opacity: 1 }}
          exit={{ opacity: 0 }}
          className="fixed inset-0 bg-black/60 backdrop-blur-sm z-40 lg:hidden"
          onClick={onClose}
        />
      )}

      {/* Sidebar */}
      <aside
        className={cn(
          'fixed top-0 left-0 bottom-0 w-[280px] z-50',
          'bg-[#0f0f1a]/95 backdrop-blur-md border-r border-white/5',
          'flex flex-col',
          'transition-transform duration-300 ease-in-out',
          // Mobile: hidden by default, shown when isOpen
          isMobile ? (isOpen ? 'translate-x-0' : '-translate-x-full') : 'translate-x-0',
          // Desktop: always visible, static positioning
          'lg:z-30 lg:translate-x-0'
        )}
      >
        {/* Logo */}
        <div className="p-6 border-b border-white/5">
          <Link href="/" className="flex items-center gap-3" onClick={onClose}>
            <div className="w-10 h-10 rounded-xl overflow-hidden flex-shrink-0">
              <Image
                src="/logo.png"
                alt="ANOA Network"
                width={100}
                height={100}
                className="w-full h-full object-cover"
                priority
              />
            </div>
            <div>
              <span className="text-lg font-bold text-white">ANOA Network</span>
              <span className="block text-xs text-white/40 font-semibold text-lg font-bold mb-2 bg-gradient-to-r from-cyan-500 via-purple-500 to-purple-500 bg-clip-text text-transparent">
                Trustless AI Agents
              </span>
            </div>
          </Link>
        </div>

        {/* Main Navigation */}
        <nav className="flex-1 p-4 space-y-1 overflow-y-auto">
          {navigation.map((item) => {
            const isActive = pathname === item.href;
            return (
              <Link
                key={item.name}
                href={item.href}
                onClick={onClose}
                className={cn(
                  'flex items-center gap-3 px-4 py-3 rounded-xl text-sm font-medium transition-all',
                  isActive
                    ? 'bg-primary-500/20 text-primary-400 border border-primary-500/30'
                    : 'text-white/60 hover:text-white hover:bg-white/5'
                )}
              >
                <span
                  className={cn(
                    isActive ? 'text-primary-400' : 'text-white/40'
                  )}
                >
                  {item.icon}
                </span>
                {item.name}
                {isActive && (
                  <span className="ml-auto w-1.5 h-1.5 rounded-full bg-primary-400" />
                )}
              </Link>
            );
          })}
        </nav>

        {/* Secondary Navigation */}
        <div className="p-4 border-t border-white/5 space-y-1">
          {secondaryNavigation.map((item) => {
            const isActive = pathname === item.href;
            return (
              <Link
                key={item.name}
                href={item.href}
                onClick={onClose}
                className={cn(
                  'flex items-center gap-3 px-4 py-2.5 rounded-xl text-sm font-medium transition-all',
                  isActive
                    ? 'text-white bg-white/5'
                    : 'text-white/40 hover:text-white/60'
                )}
              >
                {item.icon}
                {item.name}
              </Link>
            );
          })}
        </div>

        {/* Stats Card */}
        <SidebarStatsCard />
      </aside>
    </>
  );
}
